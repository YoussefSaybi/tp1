public class tp1exer1 {
    float longeur;
    float largeur;

    public tp1exer1() {
    }

    float perimetre(float a, float b) {
        return a * 2.0F + b * 2.0F;
    }

    float diagonale(float a, float b) {
        return a * a + b * b;
    }

    float doubler(float a, float b) {
        return a * 2.0F;
    }
}
